class Encoder(nn.Module):
    """
    Encoder.
    """

    def __init__(self, encoded_image_size=14):
        super(Encoder, self).__init__()
        self.enc_image_size = encoded_image_size

        resnet = torchvision.models.resnet101(pretrained=False)  # pretrained ImageNet ResNet-101

        # Remove linear and pool layers (since we're not doing classification)
        modules = list(resnet.children())[:-2]
        self.resnet = nn.Sequential(*modules)
        self.resnet_block_1 = nn.Sequential(*modules[0:5])  # out_channel = 256
        self.nonlocalblock1_1 = nonlocalnet(256)

        self.resnet_block_2 = nn.Sequential(*modules[5])  # out_channel = 512
        self.nonlocalblock2_1 = nonlocalnet(512)

        self.resnet_block_3 = nn.Sequential(*modules[6])  # out_channel = 1024
        self.nonlocalblock3_1 = nonlocalnet(1024)

        self.resnet_block_4 = nn.Sequential(*modules[7])  # out_channel = 2048
        # self.nonlocalblock4_1 = nonlocalnet(2048)

        # Resize image to fixed size to allow input images of variable size
        self.adaptive_pool = nn.AdaptiveAvgPool2d((encoded_image_size, encoded_image_size))

        self.fine_tune()

    def forward(self, images):
        """
        Forward propagation.

        :param images: images, a tensor of dimensions (batch_size, 3, image_size, image_size)
        :return: encoded images
        """
        out = self.resnet_block_1(images)  # (batch_size, 2048, image_size/32, image_size/32)
        out = self.nonlocalblock1_1(out)

        out = self.resnet_block_2(out)
        out = self.nonlocalblock2_1(out)

        out = self.resnet_block_3(out)
        out = self.nonlocalblock3_1(out)

        out = self.resnet_block_4(out)
        out = self.adaptive_pool(out)  # (batch_size, 2048, encoded_image_size, encoded_image_size)
        out = out.permute(0, 2, 3, 1)  # (batch_size, encoded_image_size, encoded_image_size, 2048)
        return out

    def fine_tune(self, fine_tune=True):
        """
        Allow or prevent the computation of gradients for convolutional blocks 2 through 4 of the encoder.

        :param fine_tune: Allow?
        """
        for p in self.resnet_block_1.parameters():
            p.requires_grad = False
